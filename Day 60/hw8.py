def plus():
    mathematical_operation = input("what operation do you want to do?:")

    if  mathematical_operation == "*":
        print(10*5+5)

    if mathematical_operation == "+":
        print(10+5+5)

    if  mathematical_operation == "-":
        print(10-5+5)
    
    if mathematical_operation == "/":
        print(10/5+5)

plus()